#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int i;
	int len;

	i = 0;
	len = ft_strlen(s);
	while (i <= len)
	{
		if (s[len] == c)
			return ((char*)s + len);
		len--;
	}
	return (NULL);
}
