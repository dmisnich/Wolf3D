#include "libft.h"

int		ft_char_len(const char *s, int j, char c)
{
	size_t		i;

	i = 0;
	while (s[j] == c)
		j++;
	while (s[j] != '\0' && s[j] != c)
	{
		i++;
		j++;
	}
	return (i);
}
